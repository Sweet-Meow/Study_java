package java1;
// 데이터와 연산


public class java1_01 {

    public static void main(String[] args) {
        System.out.println(6);  // 숫자(Number)
        System.out.println("Six / 여섯");  // 문자(String)

        System.out.println("6");  // 문자(String)

        System.out.println(6 + 6); // 숫자연산
        System.out.println("6" + "6");  // 문자연산

        System.out.println("1111".length()); // 문자열길이
        // .length() : 해당 문자열의 길이 출력 함수

        System.out.println("Hello Java"); // 문자열(String)
        System.out.println('H');  // 문자열(Char)


    }
}
